const CACHE_NAME = 'peon-pwa-cache-v1';
const urlsToCache = [
  './',
  './Peon_especialista_2025_ios.htm',
  './manifest.json',
  './icon.png',
  './icon-512x512.png'
];

self.addEventListener('install', event => {
  event.waitUntil(
    caches.open(CACHE_NAME)
      .then(cache => {
        return cache.addAll(urlsToCache);
      })
  );
});

self.addEventListener('fetch', event => {
  event.respondWith(
    caches.match(event.request)
      .then(response => {
        if (response) {
          return response;
        }
        return fetch(event.request);
      })
  );
});